const offCache = {}

export async function searchOpenFoodFacts(query) {
  if (offCache[query]) return offCache[query]
  try {
    const res = await fetch(
      `https://world.openfoodfacts.org/cgi/search.pl?search_terms=${encodeURIComponent(query)}&json=1&page_size=5`,
      { signal: AbortSignal.timeout(4000) }
    )
    const data = await res.json()
    const results = (data.products || []).map(p => ({
      id: 'off_' + p.code,
      name: p.product_name || query,
      brand: p.brands || '',
      serving: '100g',
      servingGrams: 100,
      calories: Math.round(p.nutriments['energy-kcal_100g'] || 0),
      protein: Math.round((p.nutriments['proteins_100g'] || 0) * 10) / 10,
      carbs: Math.round((p.nutriments['carbohydrates_100g'] || 0) * 10) / 10,
      fats: Math.round((p.nutriments['fat_100g'] || 0) * 10) / 10,
      isOnline: true,
    })).filter(r => r.name && (r.calories > 0 || r.protein > 0))
    offCache[query] = results
    return results
  } catch {
    return []
  }
}

export function searchLocalFoods(query) {
  const q = query.toLowerCase().trim()
  if (!q) return []
  return seedFoods.filter(f =>
    f.name.toLowerCase().includes(q) ||
    f.name.toLowerCase().startsWith(q[0])
  ).slice(0, 8)
}

function levenshtein(a, b) {
  const m = a.length, n = b.length
  const dp = Array.from({ length: m + 1 }, () => Array(n + 1).fill(0))
  for (let i = 0; i <= m; i++) dp[i][0] = i
  for (let j = 0; j <= n; j++) dp[0][j] = j
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      dp[i][j] = a[i - 1] === b[j - 1]
        ? dp[i - 1][j - 1]
        : 1 + Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1])
    }
  }
  return dp[m][n]
}

export function fuzzyMatchFood(query) {
  const q = query.toLowerCase().trim()
  if (!q) return null
  const words = q.split(/\s+/)
  let best = null, bestScore = Infinity
  for (const f of seedFoods) {
    const name = f.name.toLowerCase()
    let score = levenshtein(q, name)
    if (words.some(w => name.includes(w) && w.length > 2)) score -= 5
    if (name.startsWith(q)) score -= 3
    if (score < bestScore) { bestScore = score; best = f }
  }
  return bestScore <= 4 ? best : null
}
